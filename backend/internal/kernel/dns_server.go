//go:build linux

package kernel

import (
	"fmt"
	"log"
	"os"
	"os/exec"
	"strings"

	"pigate/internal/model"
)

type RealDNSServerManager struct {
	configPath string
}

func NewRealDNSServerManager() *RealDNSServerManager {
	return &RealDNSServerManager{
		configPath: "/etc/dnsmasq.d/pigate-dns.conf",
	}
}

// validateDnsmasqConfig checks configuration syntax using dnsmasq --test
func (m *RealDNSServerManager) validateDnsmasqConfig(tempFile string) error {
	cmd := exec.Command("dnsmasq", "--test", "--conf-file="+tempFile)
	output, err := cmd.CombinedOutput()
	if err != nil {
		return fmt.Errorf("dnsmasq config validation failed: %s (err: %w)", string(output), err)
	}
	return nil
}

func (m *RealDNSServerManager) ApplyZones(zones []model.DNSZone, interfaces []string, upstreamServers []string) error {
	log.Printf("[DNS Server] ApplyZones called with %d zones, %d interfaces, %d upstream servers", len(zones), len(interfaces), len(upstreamServers))

	var sb strings.Builder
	sb.WriteString("# /etc/dnsmasq.d/pigate-dns.conf — Generated by PiGate\n\n")

	// Explicit upstream resolvers. We deliberately combine `no-resolv` (ignore
	// /run/dnsmasq/resolv.conf, which is broken on Debian when the resolvconf
	// hook fails) with explicit `server=` lines drawn from System DNS. Only emit
	// `no-resolv` when we actually have at least one upstream — emitting it with
	// zero `server=` lines would leave dnsmasq with no upstream at all (worse than
	// the default). Note: `no-resolv` is process-global, affecting the whole
	// dnsmasq instance (including the DHCP config), which is fine here.
	if len(upstreamServers) > 0 {
		sb.WriteString("# Upstream resolvers (from System DNS)\n")
		sb.WriteString("no-resolv\n")
		for _, ip := range upstreamServers {
			ip = strings.TrimSpace(ip)
			if ip == "" {
				continue
			}
			sb.WriteString(fmt.Sprintf("server=%s\n", ip))
		}
		sb.WriteString("\n")
	}

	for _, zone := range zones {
		if !zone.Enabled {
			continue
		}

		zoneName := strings.TrimSpace(zone.ZoneName)
		if zoneName == "" {
			continue
		}

		// Defense-in-depth: never let an un-validated zone reach the config file,
		// even if a malformed value somehow persisted to the DB. The handler and
		// config-import paths reject these up front; this is the last line.
		if err := model.ValidateDNSZone(zone); err != nil {
			log.Printf("[DNS Server] Skipping invalid zone %q: %v", zoneName, err)
			continue
		}

		sb.WriteString(fmt.Sprintf("# Zone: %s\n", zoneName))

		if zone.IsAuthoritative {
			// Mark the zone as local-only. `local=/<zone>/` tells dnsmasq to answer
			// this zone from its own records (host-record/cname/...) and return
			// NXDOMAIN for unknown names instead of forwarding upstream — without
			// putting the whole resolver into authoritative-only mode (which the old
			// `auth-zone=`/`auth-server=` pair did, disabling recursion globally and
			// causing every external query to be REFUSED). `local=/zone/` is a
			// per-zone directive designed to be repeated, so multiple zones coexist
			// (fixes the multi-zone bug). We intentionally do NOT emit `domain=` per
			// zone — that is a global directive already set once in pigate-base.conf.
			sb.WriteString(fmt.Sprintf("local=/%s/\n", zoneName))

			for _, rec := range zone.Records {
				if err := model.ValidateDNSRecord(rec); err != nil {
					log.Printf("[DNS Server] Skipping invalid record %q (type %s) in zone %q: %v", rec.Name, rec.Type, zoneName, err)
					continue
				}

				name := strings.TrimSpace(rec.Name)
				fullName := zoneName
				if name != "" && name != "@" && name != zoneName {
					fullName = fmt.Sprintf("%s.%s", name, zoneName)
				}

				switch strings.ToUpper(rec.Type) {
				case "A", "AAAA":
					// address=/fqdn/ip
					sb.WriteString(fmt.Sprintf("host-record=%s,%s\n", fullName, rec.Value))
				case "CNAME":
					// cname=fqdn,target
					// dnsmasq requires the CNAME target to be a FQDN it can resolve.
					// If the user entered a short name (no dot), treat it as a name
					// within this zone and append the zone name. A value that already
					// contains a dot is treated as a FQDN as-is (trailing dot stripped,
					// since dnsmasq does not use it). We normalize only at generation
					// time — the DB keeps the user's original value untouched.
					target := strings.TrimSuffix(strings.TrimSpace(rec.Value), ".")
					if target != "" && !strings.Contains(target, ".") {
						target = fmt.Sprintf("%s.%s", target, zoneName)
					}
					sb.WriteString(fmt.Sprintf("cname=%s,%s\n", fullName, target))
				case "MX":
					// mx-host=fqdn,target,pref
					pref := "10"
					target := rec.Value
					parts := strings.Fields(rec.Value)
					if len(parts) >= 2 {
						pref = parts[0]
						target = parts[1]
					}
					sb.WriteString(fmt.Sprintf("mx-host=%s,%s,%s\n", fullName, target, pref))
				case "TXT":
					// txt-record=fqdn,"text"
					sb.WriteString(fmt.Sprintf("txt-record=%s,\"%s\"\n", fullName, rec.Value))
				case "PTR":
					// ptr-record=name,fqdn
					sb.WriteString(fmt.Sprintf("ptr-record=%s,%s\n", fullName, rec.Value))
				}
			}
		} else {
			// Forward Zone
			// server=/zone/forward_to
			if zone.ForwardTo != "" {
				sb.WriteString(fmt.Sprintf("server=/%s/%s\n", zoneName, zone.ForwardTo))
			}
		}
		sb.WriteString("\n")
	}

	configData := sb.String()

	// Write to temp file for validation
	tempFile := "/tmp/pigate-dns-test.conf"
	if err := os.WriteFile(tempFile, []byte(configData), 0644); err != nil {
		return fmt.Errorf("failed to write temp DNS config: %w", err)
	}
	defer os.Remove(tempFile)

	// Validate syntax
	if err := m.validateDnsmasqConfig(tempFile); err != nil {
		return err
	}

	// Save to production
	if err := os.WriteFile(m.configPath, []byte(configData), 0644); err != nil {
		return fmt.Errorf("failed to write production dnsmasq DNS config: %w", err)
	}

	log.Println("[DNS Server] DNS config applied and validated successfully")
	return m.ReloadConfig()
}

func (m *RealDNSServerManager) ReloadConfig() error {
	return RestartServiceViaDBus("dnsmasq.service")
}

func (m *RealDNSServerManager) ClearCache() error {
	log.Println("[DNS Server] Clearing DNS cache via dnsmasq restart")
	return m.ReloadConfig()
}
